public class MaiorEntreNumeros {
    public static void main(String[] args) {
    int num1 = 10;
    int num2 = 40;
    int maiorNumero;

    if (num1 >= num2 ) {
        System.out.println(maiorNumero = num1);
    } else {
        System.out.println(maiorNumero = num2);
    }
    
    System.out.println("O maior número é: " + maiorNumero);

    }
}
