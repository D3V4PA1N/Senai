public class MaiorDeDoisNumeros {
 /**
 * @param args
 */
public static void main(String[] args) {
    int num1 = 100;
    int num2 = 40;
    int maiorNumero;

    if (num2 >= num1 ) {
        System.out.println(maiorNumero = num2);
    } else {
        System.out.println(maiorNumero = num1);
    }
    
    System.out.println("O maior número é: " + maiorNumero);

   }   
 }
